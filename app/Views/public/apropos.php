                <h5 class="text-muted">Hébergement VPS 2025</h5>
                <p class="small text-muted text-justifier">
                    Un VPS (Virtual Private server), serveur privée virtuel en français, est une méthode utilisée en cloud par les sociétés d'hébergement
                    permettant une haute flexibilité des systèmes d'exploitation grâce à la conteneurisation (Docker). Avec une seule machine, on peut isoler des dizaines
                    de serveurs fonctionnant parfaitement bien. J'ai utilisé cette méthode en version <i>home</i> pour héberger mon site web Portfolio. 
                    Le site était d'abord hébergé sur un serveur Debian. C'était une première expérience de déploiement, mais, elle ne fût pas facile à réaliser.
                    Bien qu'il y avait un hook d'automatisation pour le CI/CD, c'était du bricolage, instable et difficiel à maintenir. Cette Solution a été mise
                    en place vers juin 2025 et a durée jusqu'à novembre de la même année (environ 6 mois). 
                </p>
