                <h5 class="mt-3 text-muted">Hébergement VPS 2026</h5>
                <p class="small text-muted text-justifier">Fin janvier 2026, j'ai décidé de
                    passer à une solution plus solide et sécurisée, c'est là que j'ai mis en place une infrastructure
                    plus sérieuse avec Ubuntu Server. Entre temps, alors que je n'avais pas de nom de domaine
                    et qu'il fallait saisir l'IP publique de ma box internet ainsi que le port, j'ai pris l'initiative 
                    de configurer le nom de sous domaine gratuit proposé par mon fournisseur d'accès à internet (FAI), Free.
                    Toujours en virtuelle, cette solution bien plus performante que la première, m'a permis de me familiariser
                    avec les outils linux (Terminal Bash), les services de sécurité (SSH, pare-feu UFW, IPTABLES), et les services web 
                    (Nginx, Apache), et d'autres services Linux. La solution est documentée dans une des activités du Portfolio.
                </p>
